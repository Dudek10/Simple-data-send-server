import time
import socket
import os
import struct
import hashlib

HOST = '127.0.0.1'
PORT = 5000
MAX_SIZE = 10485760
ALLOWED_EXT = {'jpg', 'jpeg', 'png', 'gif', 'txt'}

def recv_until_newline(sock):
    data = b""
    while not data.endswith(b"\n"):
        part = sock.recv(1024)
        if not part:
            break
        data += part
    return data.strip()

socket_client = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
socket_client.connect((HOST, PORT))

i = 0
while i < 3:
    password = input("üîë Podaj has≈Ço dostƒôpu: ").strip()
    password_hash = hashlib.sha512(password.encode()).digest()
    socket_client.send(password_hash)
    response = socket_client.recv(32)
    if response == b"OK":
        print("‚úÖ Has≈Ço poprawne.\n")
        break
    elif response == b'PASSWORD_NOT_PROVIDED':
        print("Klient nie poda≈Ç has≈Ça.\n")
        time.sleep(3)
        socket_client.close()
        quit()
    elif response == b'WRONG_PASSWORD':
        print("‚ùå B≈Çƒôdne has≈Ço. Spr√≥buj ponownie.\n")
        i += 1
    if i == 3:
        print("Zbyt wiele pr√≥b wpisania has≈Ça! Ko≈Ñczƒô sesjƒô")
        time.sleep(3)
        socket_client.close()
